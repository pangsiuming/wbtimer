public class GovBaseException extends CRMException {

    public GovBaseException(String expCode, String expDesc) {
        super(expCode, expDesc);
    }

    public GovBaseException(String expCode, String expDesc, Object... objs) {
        super(expCode, expDesc, objs);
    }

    public GovBaseException(String expCode, String expDesc, Throwable cause) {
        super(expCode, expDesc, cause);
    }

    public String getMessage() {
        return getExpDesc();
    }
}

